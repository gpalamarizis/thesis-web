const PHONE_TYPES   = ['Οικίας', 'Γραφείου', 'Κινητό', 'Fax', 'Άλλο'];

async function seedLookups(client, orgId) {
  const batch = async (sql, rows) => {
    for (const r of rows) await client.query(sql, r);
  };

  await batch(
    `INSERT INTO diadikasies (organization_id, name) VALUES ($1, $2)`,
    DIADIKASIES.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO dikastiria_exelixi_energeias (organization_id, name) VALUES ($1, $2)`,
    EXELIXIS_ENERGEIAS.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO thesi (organization_id, name) VALUES ($1, $2)`,
    THESI_STIN_YPOTHESI.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO ypotheseis_onomasies (organization_id, name) VALUES ($1, $2)`,
    YPOTHESEIS_ONOMASIES.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO theseis_arxeiothetisis (organization_id, name, perigrafi) VALUES ($1, $2, $3)`,
    THESEIS_ARXEIOTHETISIS.map(t => [orgId, t.name, t.perigrafi])
  );

  await batch(
    `INSERT INTO eidos_sxesis (organization_id, name) VALUES ($1, $2)`,
    EIDOS_SXESIS.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO pagia_exoda (organization_id, name) VALUES ($1, $2)`,
    PAGIA_EXODA.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO amoives (organization_id, name, amount) VALUES ($1, $2, $3)`,
    AMOIVES.map(a => [orgId, a.name, a.amount])
  );

  await batch(
    `INSERT INTO countries (organization_id, name) VALUES ($1, $2)`,
    COUNTRIES.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO cities (organization_id, name) VALUES ($1, $2)`,
    CITIES.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO address_type (organization_id, address_type) VALUES ($1, $2)`,
    ADDRESS_TYPES.map(n => [orgId, n])
  );

  await batch(
    `INSERT INTO phone_types (organization_id, phone_type) VALUES ($1, $2)`,
    PHONE_TYPES.map(n => [orgId, n])
  );

  // Empty org settings row
  await client.query(
    `INSERT INTO stoixeia_epixeirisis (organization_id) VALUES ($1)
     ON CONFLICT (organization_id) DO NOTHING`,
    [orgId]
  );
}

module.exports = { seedLookups };
